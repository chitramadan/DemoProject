package core.aem;

/**
 * The Class MultifieldDetailsBean.
 */
public class MultifieldDetailsBean {

    /** The text. */
    private String text;

    /** The text description. */
    private String textDescription;

    /** The image. */
    private String supportingImage;

    /**
     * Gets the text.
     * 
     * @return the text
     */
    public String getText() {
        return text;
    }

    /**
     * Sets the text.
     * 
     * @param text
     *            the text
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * Gets the text description.
     * 
     * @return the textDescription
     */
    public String getTextDescription() {
        return textDescription;
    }

    /**
     * Sets the text description.
     * 
     * @param textDescription
     *            the textDescription
     */
    public void setTextDescription(String textDescription) {
        this.textDescription = textDescription;
    }

    /**
     * Gets the image.
     * 
     * @return the supportingImage
     */
    public String getSupportingImage() {
        return supportingImage;
    }

    /**
     * Sets the image.
     * 
     * @param supportingImage
     *            the supportingImage
     */
    public void setSupportingImage(String supportingImage) {
        this.supportingImage = supportingImage;
    }

}
